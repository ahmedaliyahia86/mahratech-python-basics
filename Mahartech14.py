# for loops examples: Using range() arguments

for n in range(5):
    print(n)
print('----------')
for i in range(7,11):
    print(i)
print('----------')
sum = 0
for x in range(7,11):
    sum += x            # ==  sum = sum + x
    print(sum)
print('----------')
for z in range(2,11,3):
    print(z)
